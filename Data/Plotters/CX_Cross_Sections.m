
clear all
close all

f = fopen('../CX_CS.dat'); 
d = fscanf(f,'%f %f %f %f %f %f %f %f %f %f %f %f',[12,inf]); 
d = d'; 

BTOM = 5.29177249D-11;  

C = 1/BTOM^2;
%C = 1e4;

E 			 = d(:,1); 
Hp_CO2   = d(:,2)*C; 
Hp_O2    = d(:,3)*C; 
Hp_O     = d(:,4)*C;
Hp_He    = d(:,5)*C;
Hp_H     = d(:,6)*C;
Hep_H    = d(:,7)*C;
Hep_He   = d(:,8)*C;
Hepp_CO2 = d(:,9)*C; 
Hepp_1H  = d(:,10)*C;
Hepp_1He = d(:,11)*C;
Hepp_2He = d(:,12)*C;

figure
h = semilogy(E,Hp_CO2,'k', ...
         E,Hp_O2,     'm', ...     
         E,Hp_O,      'b', ...
         E,Hp_He,     'r', ...
         E,Hp_H,      'g', ...
         E,Hep_H,     'c', ...
         E,Hep_He,    'k--', ...
         E,Hepp_CO2,  'm--', ...
         E,Hepp_1H,   'b--', ...
         E,Hepp_1He,  'r--',...
         E,Hepp_2He,  'g--'); 
set(h,'LineWidth',2.5)
set(gca,'FontSize',10)
axis([100 1000 1e-6 2e2]) 
xlabel('Energy [eV]')
ylabel('Charge Exchange Cross Section [a_{0}^2]')
legend('H^{+}+CO_{2} -> H+CO_{2}^{+}', ...
       'H^{+}+O_{2} -> H+O_{2}^{+}', ...
       'H^{+}+O -> H+O^{+}', ...
       'H^{+}+He -> H+He^{+}', ...
       'H^{+}+H -> H+H^{+}', ...
       'He^{+}+H -> He+H^{+}', ...
       'He^{+}+He -> He+He^{+}', ...
       'He^{2+}+CO2 -> He+Co2^{2+}', ... 
       'He^{+2}+H -> He^{+}+H^{+}', ...
       'He^{+2}+He -> He^{+}+He^{+}', ...
       'He^{+2}+He -> He+He^{+2}', ...
       'Location','Best')
print -depsc2 ./Plots/CX_Cross_Sections_All.eps
print -djpeg100 ./Plots/CX_Cross_Sections_All.jpeg

figure
h1 = semilogy(E,Hp_CO2,    'k', ...
              E,Hp_O,      'b', ...
              E,Hp_He,     'r', ...
              E,Hp_H,      'g', ...
              E,Hepp_CO2,  'k--', ...
              E,Hepp_2He,  'r--'); 
set(h1,'LineWidth',2.5)
set(gca,'FontSize',12)
axis([200 1000 1e-6 2e2]) 
xlabel('Energy [eV]')
ylabel('Charge Exchange Cross Section [a_{0}^2]')
legend('H^{+}+CO_{2} -> H+CO_{2}^{+}', ...
       'H^{+}+O -> H+O^{+}', ...
       'H^{+}+He -> H+He^{+}', ...
       'H^{+}+H -> H+H^{+}', ...
       'He^{2+}+CO2 -> He+Co2^{2+}', ... 
       'He^{+2}+He -> He+He^{+2}', ...
       'Location','Best')
print -depsc2 ./Plots/CX_Cross_Sections_Select.eps
print -djpeg100 ./Plots/CX_Cross_Sections_Select.jpeg


